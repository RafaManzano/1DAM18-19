public class Tema3if {
	
	public static void main (String[] args) {
		int a = 4;
		
		if (a == 4) {
			System.out.println("La variable es igual a 4");
		}
		
		if (a > 5) {
			System.out.println("La variable es mayor que 5");
		}
		
		else  {
			System.out.println("La variable es menor que 6");
		}
	}
}

